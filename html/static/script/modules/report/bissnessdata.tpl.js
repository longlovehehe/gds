$("a.bissness").on("click",function(){
    $("div.select_time").removeClass("none");
    $("div.content").addClass("none");
   $("div.charts10").removeClass("none");
   $("div.charts11").removeClass("none");
   $("div.charts12").removeClass("none");
   $("div.charts13").removeClass("none");
   $("div.charts14").removeClass("none");
   $("div.charts15").removeClass("none");
   $("a.charts_nav").each(function(){
       $(this).removeClass("active");
   }); 
   $(this).addClass("active");
   $("input[name=status]").val("bissness");
   $("div.bissness_data").removeClass("none");
   $("input[name=intercom_recording]").prop("checked","checked");
    var od=1;
    var ld=7;
   $("div.open_data input[type=checkbox]").each(function(){
        if($(this).is(":checked")){
            $(this).removeAttr("checked");
        }
         $("div.charts"+od).addClass("none");
        od++;
    });
    $("div.live_data input[type=checkbox]").each(function(){
        if($(this).is(":checked")){
            $(this).removeAttr("checked");
        }
         $("div.charts"+ld).addClass("none");
        ld++;
    });
   $("div.open_data").addClass("none");
   $("div.live_data").addClass("none");
   $("div.gprs_data").addClass("none");
   $("div.terminal_data").addClass("none");
   send();
});
function get_intercom_recording(obj){
    $.ajax({
            url:"?m=report&a=get_intercom_recording",
            dataType:"html",
            async:true,
            data:{
                ep_id:$('input[name=ep_id]').val(),
                checkp:$('select[name=checkp]').val(),
                date_type:"day",
                data_type:"_intercom_recording",
                table_type:"intercom_recording_table",
                index:"10",
                start:$("input[name=start]").val(),
                end:$("input[name=end]").val()
            },
            success:function(res){
                obj.html(res);
            }
        });
}
